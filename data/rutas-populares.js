/**
 * Parsea slug de ruta formato: [origen]_[destino]
 * Retorna { origen, destino } o null si no es válido
 */
export function parseRutaSlug(slug) {
  if (!slug || typeof slug !== 'string') return null;
  const parts = slug.split('_');
  if (parts.length !== 2) return null;
  return { origen: parts[0], destino: parts[1] };
}

/**
 * Construye slug de ruta a partir de origen y destino
 */
export function buildRutaSlug(origen, destino) {
  if (!origen || !destino) return null;
  return `${origen}_${destino}`;
}

/**
 * Busca una ruta en rutasPopulares por origen y destino
 */
export function findRuta(origen, destino) {
  return rutasPopulares.find(r => r.origen === origen && r.destino === destino) || null;
}

/**
 * Retorna rutas relacionadas (mismo origen o destino)
 */
export function getRelatedRoutes(origen, destino, limit = 3) {
  const related = rutasPopulares.filter(
    r => (r.origen === origen || r.destino === destino) && !(r.origen === origen && r.destino === destino)
  );
  return related.slice(0, limit);
}

/**
 * Retorna rutas al Estadio Azteca (vía Tasqueña → Tren Ligero)
 */
export function getRutasAEstadio() {
  return rutasPopulares.filter(r => r.destino === 'tasquena');
}

/**
 * Retorna rutas turísticas (excluyendo Estadio Azteca)
 */
export function getRutasTuristicas() {
  return rutasPopulares.filter(r => r.destino !== 'tasquena');
}
